public class Car {

    String make = "Fiat";
    String model = "Punto";
    String color = "Blue";
    int year = 1999;

    public String whatEver(){
        return make + " " + model + " " + color + " " + year;
    }
    public String toString(){
        return make + " " + model + " " + color + " " + year;
    }
}
