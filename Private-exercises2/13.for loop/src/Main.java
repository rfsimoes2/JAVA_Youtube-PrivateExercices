public class Main {
    public static void main(String[] args) {

            //for loop = executes a block of code limited to the amount of times

        for (int i = 10; i >= 0; i-=2) {  //In this example i is decremented by 2 each time
                System.out.println(i);
            }
        System.out.println("Happy new year!!!");

    }
}